    private void openFile(){
        try {
            FileReader rf = new FileReader(file);
            BufferedReader rb = new BufferedReader(rf);
            String line;
            textSource.setText("");
            
            
            while ((line=rb.readLine())!=null) {   
                textLine.append(i+"\n");   
                textSource.append(line + "\n");
                i++;
            }
            rb.close();
        } catch (Exception e) {
            textMessenger.setForeground(Color.red);
            textMessenger.setText(e.getMessage());
        }
        
    }
    private void saveFile(){
        try {
            FileWriter wf = new FileWriter(file);
            BufferedWriter bf = new BufferedWriter(wf);
            bf.write(textSource.getText());
            textMessenger.setForeground(Color.blue);
            textMessenger.setText("file saved ...");
            bf.close();
        } catch (Exception e) {
            textMessenger.setForeground(Color.red);
            textMessenger.setText(e.getMessage());
        }
        
    }
